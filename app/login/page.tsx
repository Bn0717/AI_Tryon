// app/login/page.tsx
'use client';

import { useState, useEffect } from 'react';
import { useRouter } from 'next/navigation';
import { signIn, signInWithGoogle, resetPassword } from '@/lib/firebase/auth';
import { useAuth } from '@/lib/contexts/AuthContext';

const colors = {
  cream: '#F8F3EA',
  navy: '#0B1957',
  peach: '#FFDBD1',
  pink: '#FA9EBC'
};

export default function LoginPage() {
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [error, setError] = useState<string | null>(null);
  const [loading, setLoading] = useState(false);
  const [showResetModal, setShowResetModal] = useState(false);
  const [resetEmail, setResetEmail] = useState('');
  const [resetSuccess, setResetSuccess] = useState(false);
  const [resetLoading, setResetLoading] = useState(false);
  const router = useRouter();
  const { user } = useAuth();

  // Redirect if already logged in
  useEffect(() => {
    if (user) {
      router.push('/profile');
    }
  }, [user, router]);

  const handleLogin = async (e: React.FormEvent) => {
    e.preventDefault();
    setError(null);

    // Validate email format
    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    if (!emailRegex.test(email)) {
      setError('Please enter a valid email address');
      setLoading(false);
      return;
    }

    setLoading(true);

    const { user, error: signInError } = await signIn(email, password);

    if (signInError) {
      // Make Firebase errors more user-friendly
      if (signInError.includes('user-not-found') || signInError.includes('wrong-password') || signInError.includes('invalid-credential')) {
        setError('Invalid email or password. Please try again.');
      } else if (signInError.includes('too-many-requests')) {
        setError('Too many failed attempts. Please try again later or reset your password.');
      } else if (signInError.includes('invalid-email')) {
        setError('Please enter a valid email address');
      } else {
        setError(signInError);
      }
      setLoading(false);
      return;
    }

    if (user) {
      router.push('/profile');
    }
  };

  const handleGoogleLogin = async () => {
    setError(null);
    setLoading(true);

    try {
      const { user, error: googleError } = await signInWithGoogle();

      if (googleError) {
        // Check if user closed the popup
        if (googleError.includes('popup-closed-by-user') || googleError.includes('cancelled')) {
          setError('Sign-in cancelled. Please try again.');
        } else {
          setError(googleError);
        }
        setLoading(false);
        return;
      }

      if (user) {
        // Add small delay to ensure auth state is updated
        setTimeout(() => {
          router.push('/profile');
        }, 500);
      } else {
        setLoading(false);
      }
    } catch (err: any) {
      setError('An unexpected error occurred. Please try again.');
      setLoading(false);
    }
  };

  const handlePasswordReset = async () => {
    if (!resetEmail.trim()) {
      setError('Please enter your email address');
      return;
    }

    setResetLoading(true);
    setError(null);

    const { success, error: resetError } = await resetPassword(resetEmail);

    if (resetError) {
      setError(resetError);
      setResetLoading(false);
      return;
    }

    if (success) {
      setResetSuccess(true);
      setResetLoading(false);
      setTimeout(() => {
        setShowResetModal(false);
        setResetSuccess(false);
        setResetEmail('');
      }, 3000);
    }
  };

  return (
    <div className="min-h-screen flex" style={{ backgroundColor: colors.cream }}>
      {/* Left Side - Branding */}
      <div className="hidden lg:flex lg:w-1/2 items-center justify-center p-12" style={{ backgroundColor: colors.navy }}>
        <div className="max-w-md">
          <h1 className="text-5xl font-black mb-4 text-white">
            FitCheck
          </h1>
          <p className="text-xl text-white opacity-80 mb-8">
            Never buy the wrong size again
          </p>
          <div className="space-y-4">
            <div className="flex items-center gap-3 text-white">
              <div className="w-8 h-8 rounded-full flex items-center justify-center" style={{ backgroundColor: colors.pink }}>
                <svg className="w-5 h-5" style={{ color: colors.navy }} fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M5 13l4 4L19 7" />
                </svg>
              </div>
              <span>AI-powered size recommendations</span>
            </div>
            <div className="flex items-center gap-3 text-white">
              <div className="w-8 h-8 rounded-full flex items-center justify-center" style={{ backgroundColor: colors.pink }}>
                <svg className="w-5 h-5" style={{ color: colors.navy }} fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M5 13l4 4L19 7" />
                </svg>
              </div>
              <span>Virtual try-on technology</span>
            </div>
            <div className="flex items-center gap-3 text-white">
              <div className="w-8 h-8 rounded-full flex items-center justify-center" style={{ backgroundColor: colors.pink }}>
                <svg className="w-5 h-5" style={{ color: colors.navy }} fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M5 13l4 4L19 7" />
                </svg>
              </div>
              <span>Privacy-first approach</span>
            </div>
          </div>
        </div>
      </div>

      {/* Right Side - Login Form */}
      <div className="flex-1 flex items-center justify-center p-8">
        <div className="bg-white rounded-2xl shadow-sm border-2 p-8 w-full max-w-md" style={{ borderColor: colors.peach }}>
          <h2 className="text-3xl font-bold mb-2" style={{ color: colors.navy }}>
            Welcome Back
          </h2>
          <p className="text-sm mb-8" style={{ color: colors.navy, opacity: 0.6 }}>
            Sign in to access your wardrobe
          </p>

          {/* Error Message */}
          {error && (
            <div className="mb-6 p-4 rounded-lg bg-red-50 border border-red-200">
              <p className="text-sm font-medium text-red-700">{error}</p>
            </div>
          )}

          <form onSubmit={handleLogin} className="space-y-6">
            {/* Email */}
            <div>
              <label className="block text-sm font-semibold mb-2" style={{ color: colors.navy }}>
                Email
              </label>
              <input
                type="email"
                value={email}
                onChange={(e) => setEmail(e.target.value)}
                placeholder="you@example.com"
                className="w-full px-4 py-3 rounded-lg border-2 focus:outline-none"
                style={{ borderColor: colors.peach, backgroundColor: colors.cream }}
                required
                disabled={loading}
              />
            </div>

            {/* Password */}
            <div>
              <label className="block text-sm font-semibold mb-2" style={{ color: colors.navy }}>
                Password
              </label>
              <input
                type="password"
                value={password}
                onChange={(e) => setPassword(e.target.value)}
                placeholder="••••••••"
                className="w-full px-4 py-3 rounded-lg border-2 focus:outline-none"
                style={{ borderColor: colors.peach, backgroundColor: colors.cream }}
                required
                disabled={loading}
              />
            </div>

            {/* Forgot Password */}
            <div className="text-right">
              <button 
                type="button"
                onClick={() => {
                  setShowResetModal(true);
                  setResetEmail(email);
                }}
                className="text-sm font-medium hover:underline"
                style={{ color: colors.navy }}
                disabled={loading}
              >
                Forgot password?
              </button>
            </div>

            {/* Sign In Button */}
            <button
              type="submit"
              disabled={loading}
              className="w-full py-3 px-4 rounded-lg font-bold text-white transition-opacity hover:opacity-90 disabled:opacity-50 flex items-center justify-center gap-2"
              style={{ backgroundColor: colors.navy }}
            >
              {loading ? (
                <>
                  <div className="w-5 h-5 border-2 border-white border-t-transparent rounded-full animate-spin"></div>
                  Signing in...
                </>
              ) : (
                'Sign In'
              )}
            </button>
          </form>

          {/* Divider */}
          <div className="flex items-center gap-4 my-6">
            <div className="flex-1 h-px" style={{ backgroundColor: colors.peach }}></div>
            <span className="text-sm" style={{ color: colors.navy, opacity: 0.5 }}>OR</span>
            <div className="flex-1 h-px" style={{ backgroundColor: colors.peach }}></div>
          </div>

          {/* Google Sign In */}
          <button
            onClick={handleGoogleLogin}
            disabled={loading}
            className="w-full py-3 px-4 rounded-lg font-semibold border-2 transition-all hover:shadow-sm flex items-center justify-center gap-3 disabled:opacity-50 disabled:cursor-not-allowed"
            style={{ borderColor: colors.peach, backgroundColor: 'white', color: colors.navy }}
          >
            {loading ? (
              <>
                <div className="w-5 h-5 border-2 border-t-transparent rounded-full animate-spin" style={{ borderColor: colors.navy }}></div>
                Connecting...
              </>
            ) : (
              <>
                <svg className="w-5 h-5" viewBox="0 0 24 24">
                  <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"/>
                  <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"/>
                  <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"/>
                  <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"/>
                </svg>
                Continue with Google
              </>
            )}
          </button>

          {/* Sign Up Link */}
          <p className="text-center mt-6 text-sm" style={{ color: colors.navy, opacity: 0.6 }}>
            Don't have an account?{' '}
            <button 
              onClick={() => router.push('/signup')}
              className="font-semibold hover:underline"
              style={{ color: colors.navy }}
              disabled={loading}
            >
              Sign up
            </button>
          </p>
        </div>
      </div>

      {/* Password Reset Modal */}
      {showResetModal && (
        <div className="fixed inset-0 bg-black/50 backdrop-blur-sm flex items-center justify-center z-50 p-4">
          <div className="bg-white rounded-2xl shadow-2xl max-w-md w-full overflow-hidden">
            <div className="p-8">
              <div className="flex items-center justify-between mb-6">
                <h2 className="text-2xl font-bold" style={{ color: colors.navy }}>
                  Reset Password
                </h2>
                <button 
                  onClick={() => {
                    setShowResetModal(false);
                    setResetSuccess(false);
                    setError(null);
                  }}
                  disabled={resetLoading}
                  className="w-8 h-8 rounded-full flex items-center justify-center transition-colors hover:opacity-70"
                  style={{ backgroundColor: colors.cream }}
                >
                  <svg className="w-5 h-5" style={{ color: colors.navy }} fill="none" stroke="currentColor" viewBox="0 0 24 24">
                    <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M6 18L18 6M6 6l12 12" />
                  </svg>
                </button>
              </div>

              {resetSuccess ? (
                <div className="p-4 rounded-lg mb-4" style={{ backgroundColor: colors.peach }}>
                  <p className="text-sm font-medium" style={{ color: colors.navy }}>
                    ✅ Password reset email sent! Check your inbox.
                  </p>
                </div>
              ) : (
                <>
                  <p className="text-sm mb-6" style={{ color: colors.navy, opacity: 0.7 }}>
                    Enter your email address and we'll send you a link to reset your password.
                  </p>

                  {error && (
                    <div className="mb-4 p-4 rounded-lg bg-red-50 border border-red-200">
                      <p className="text-sm font-medium text-red-700">{error}</p>
                    </div>
                  )}

                  <div className="mb-6">
                    <label className="block text-sm font-semibold mb-2" style={{ color: colors.navy }}>
                      Email Address
                    </label>
                    <input
                      type="email"
                      value={resetEmail}
                      onChange={(e) => setResetEmail(e.target.value)}
                      placeholder="you@example.com"
                      className="w-full px-4 py-3 rounded-lg border-2 focus:outline-none"
                      style={{ borderColor: colors.peach, backgroundColor: colors.cream }}
                      disabled={resetLoading}
                    />
                  </div>

                  <div className="flex gap-3">
                    <button 
                      onClick={() => {
                        setShowResetModal(false);
                        setError(null);
                      }}
                      disabled={resetLoading}
                      className="flex-1 px-6 py-3 rounded-lg font-semibold transition-colors disabled:opacity-50"
                      style={{ backgroundColor: colors.cream, color: colors.navy }}
                    >
                      Cancel
                    </button>
                    <button 
                      onClick={handlePasswordReset}
                      disabled={resetLoading}
                      className="flex-1 px-6 py-3 rounded-lg font-semibold text-white transition-opacity hover:opacity-90 disabled:opacity-50 flex items-center justify-center gap-2"
                      style={{ backgroundColor: colors.navy }}
                    >
                      {resetLoading ? (
                        <>
                          <div className="w-5 h-5 border-2 border-white border-t-transparent rounded-full animate-spin"></div>
                          Sending...
                        </>
                      ) : (
                        'Send Reset Link'
                      )}
                    </button>
                  </div>
                </>
              )}
            </div>
          </div>
        </div>
      )}
    </div>
  );
}